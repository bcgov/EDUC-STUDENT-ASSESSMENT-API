CREATE TABLE STAGED_STUDENT_RESULT
(
    STAGED_STUDENT_RESULT_ID            UUID                                NOT NULL,
    ASSESSMENT_ID                       UUID                                NOT NULL,
    ASSESSMENT_FORM_ID                  UUID                                NOT NULL,
    PEN                                 VARCHAR(9)                          NOT NULL,
    MINCODE                             VARCHAR(8)                          NOT NULL,
    STAGED_STUDENT_RESULT_STATUS        VARCHAR(10)                         NOT NULL,
    COMPONENT_TYPE                      VARCHAR(1),
    OE_MARKS                            VARCHAR(152),
    MC_MARKS                            VARCHAR(240),
    CHOICE_PATH                         VARCHAR(1),
    PROVINCIAL_SPECIAL_CASE_CODE        VARCHAR(1),
    PROFICIENCY_SCORE                   NUMERIC(1),
    IRT_SCORE                           VARCHAR(7),
    ADAPTED_ASSESSMENT_CODE             VARCHAR(10),
    MARKING_SESSION                     VARCHAR(6),
    CREATE_USER                         VARCHAR(100)                        NOT NULL,
    CREATE_DATE                         TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATE_USER                         VARCHAR(100)                        NOT NULL,
    UPDATE_DATE                         TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT STAGED_STUDENT_RESULT_ID_PK PRIMARY KEY (STAGED_STUDENT_RESULT_ID),
    CONSTRAINT FK_STAGED_ADAPTED_ASSESSMENT_CODE FOREIGN KEY (ADAPTED_ASSESSMENT_CODE) REFERENCES ADAPTED_ASSESSMENT_CODE(ADAPTED_ASSESSMENT_CODE),
    CONSTRAINT FK_STAGED_ASSESSMENT_ID FOREIGN KEY (ASSESSMENT_ID) REFERENCES ASSESSMENT (ASSESSMENT_ID),
    CONSTRAINT FK_STAGED_ASSESSMENT_FORM_ID FOREIGN KEY (ASSESSMENT_FORM_ID) REFERENCES ASSESSMENT_FORM (ASSESSMENT_FORM_ID),
    CONSTRAINT FK_STAGED_PROVINCIAL_SPECIAL_CASE_CODE FOREIGN KEY (PROVINCIAL_SPECIAL_CASE_CODE) REFERENCES PROVINCIAL_SPECIAL_CASE_CODE (PROVINCIAL_SPECIAL_CASE_CODE)
);
